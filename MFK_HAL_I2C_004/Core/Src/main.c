void I2C_Write(uint8_t reg,uint8_t value)
{
	uint8_t data[2];
	data[0] = reg;
	data[1] = value;

	HAL_I2C_Master_Transmit(&hi2c1, adress, data, 2, 100);
}

// Okuma Fonksiyonu

uint8_t I2C_Read(uint8_t reg,uint8_t numberofbytes)
{
	HAL_I2C_Mem_Read(&hi2c1, adress, reg, 1, &data_rec, numberofbytes, 100);
	return data_rec;
}

void I2C_init()
{
	I2C_Read(0x00,1);

	I2C_Write(0x2D,0); // Reset all bits
	I2C_Write(0x2D,0x08); // Measure mode on

	I2C_Write(0x2A,0x01); // TAP_Z Axes Enable

	I2C_Write(0x1D,40); //62.5mg/LSB * 40 = 2.5 g'lik KUvvete tekabül eder.

	I2C_Write(0x21,32); // Basma süresini ayarla. 652us/LSB * 32 = 0.02s.

	I2C_Write(0x22,80); // Dokunma gecikmesini ayarla. 1.25ms/LSB * 80 = 0.01s.


	// Dokunma penceresi, WINDIW register'a 240 yazarak 0.3sn'ye ayarlıyorum. (.3 / .00125) Bu değer ikinci bir basmanın olabileceği, gecikme
    // süresinin sona ermesinden sonra geçen süreyi temsil eder.
	I2C_Write(0x23, 240);

	// INT MAP'i INT1 olarak ayarla.
	I2C_Write(0x2F,0);

	// INT_ENABLE single and double tap enable
	I2C_Write(0x2E,0x60);

}

